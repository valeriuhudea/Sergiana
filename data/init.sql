CREATE TABLE users (
	id INT(11) UNSIGNED AUTO_INCREMENT PRIMARY KEY, 
	nume VARCHAR(30) NOT NULL,
	timp VARCHAR(30) NOT NULL,
	operatiuni VARCHAR(30) NOT NULL,
	locatie VARCHAR(30) NOT NULL,
	societatea VARCHAR(30) NOT NULL,
	dataa2 VARCHAR(30) NOT NULL,
	ora1 VARCHAR(30) NOT NULL,
	dataa VARCHAR(30) NOT NULL,	
	timpul VARCHAR(30) NOT NULL,
	diagnosticare VARCHAR(30) NOT NULL,
	timpul2 VARCHAR(30) NOT NULL,
	observatii VARCHAR(30) NOT NULL,	
	date TIMESTAMP
);